package com.quintus.labs.smarthome.ui.activity;

public class Member {
    private String Name;
    private String code;
    public Member() {
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }
}
